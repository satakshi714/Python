def solve(a,b):
    c=a+b
    d=a-b
    e=a*b
    f=a/b
    g=a//b
    h=a%b
    i=a**b
    print(a,'+',b,'=',c)
    print(a,'-',b,'=',d)
    print(a,'*',b,'=',e)
    print(a,'/',b,'=',f)
    print(a,'//',b,'=',g)
    print(a,'%',b,'=',h)
    print(a,'**',b,'=',i)


x=int(input("Enter first value: "))
y=int(input("Enter second value: "))

solve(x,y)
            
            
